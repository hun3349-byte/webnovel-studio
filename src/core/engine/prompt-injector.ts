/**
 * Hidden CoT (logic_check) 블록 파싱 결과
 */
export interface LogicCheckResult {
  found: boolean;
  continuityCheck?: {
    previousScene: string;
    currentStart: string;
    continuityStatus: string;
  };
  characterCheck?: {
    characters: string[];
    dbVerified: boolean;
    noFakeRelations: boolean;
  };
  plotCheck?: {
    mainGoal: string;
    pacing: string;
    expectedScenes: number;
  };
  settingsCheck?: {
    worldConsistent: boolean;
    villainSafe: boolean;
  };
  rawBlock: string;
}

/**
 * AI 응답에서 Hidden CoT (<logic_check>) 블록을 파싱하고 제거
 *
 * @param content AI가 생성한 전체 텍스트
 * @returns { cleanContent: 사용자에게 보여줄 텍스트, logicCheck: 파싱된 검증 결과 }
 */
export function parseAndRemoveLogicCheck(content: string): {
  cleanContent: string;
  logicCheck: LogicCheckResult;
} {
  const logicCheckRegex = /<logic_check>([\s\S]*?)<\/logic_check>/i;
  const match = content.match(logicCheckRegex);

  if (!match) {
    return {
      cleanContent: content.trim(),
      logicCheck: { found: false, rawBlock: '' },
    };
  }

  const rawBlock = match[1].trim();

  // logic_check 블록 제거하고 남은 콘텐츠 정리
  let cleanContent = content.replace(logicCheckRegex, '').trim();

  // 블록 제거 후 앞뒤 불필요한 공백/줄바꿈 정리
  cleanContent = cleanContent.replace(/^\n+/, '').replace(/\n{3,}/g, '\n\n');

  // 각 섹션 파싱 시도
  const result: LogicCheckResult = { found: true, rawBlock };

  // [이어쓰기 검증] 섹션 파싱
  const continuityMatch = rawBlock.match(/\[이어쓰기 검증\]([\s\S]*?)(?=\[|$)/);
  if (continuityMatch) {
    const section = continuityMatch[1];
    const prevScene = section.match(/직전 회차 마지막 장면:\s*(.+)/)?.[1]?.trim();
    const startPoint = section.match(/이번 화 시작점:\s*(.+)/)?.[1]?.trim();
    const status = section.match(/연속성 확인:\s*(.+)/)?.[1]?.trim();

    if (prevScene || startPoint || status) {
      result.continuityCheck = {
        previousScene: prevScene || '',
        currentStart: startPoint || '',
        continuityStatus: status || '',
      };
    }
  }

  // [캐릭터 검증] 섹션 파싱
  const characterMatch = rawBlock.match(/\[캐릭터 검증\]([\s\S]*?)(?=\[|$)/);
  if (characterMatch) {
    const section = characterMatch[1];
    const charList = section.match(/등장 예정 캐릭터:\s*(.+)/)?.[1]?.trim();
    const dbCheck = section.match(/DB 존재 확인:\s*(.+)/)?.[1]?.trim();
    const relCheck = section.match(/임의 관계 생성 여부:\s*(.+)/)?.[1]?.trim();

    if (charList || dbCheck || relCheck) {
      result.characterCheck = {
        characters: charList ? charList.split(/[,，、]/).map(c => c.trim()) : [],
        dbVerified: dbCheck?.includes('확인') || dbCheck?.includes('예') || false,
        noFakeRelations: relCheck?.includes('없') || relCheck?.includes('확인') || false,
      };
    }
  }

  // [플롯 검증] 섹션 파싱
  const plotMatch = rawBlock.match(/\[플롯 검증\]([\s\S]*?)(?=\[|$)/);
  if (plotMatch) {
    const section = plotMatch[1];
    const goal = section.match(/이번 화 핵심 목표:\s*(.+)/)?.[1]?.trim();
    const pacing = section.match(/페이싱:\s*(.+)/)?.[1]?.trim();
    const scenes = section.match(/예상 장면 수:\s*(\d+)/)?.[1];

    if (goal || pacing || scenes) {
      result.plotCheck = {
        mainGoal: goal || '',
        pacing: pacing || 'moderate',
        expectedScenes: scenes ? parseInt(scenes, 10) : 4,
      };
    }
  }

  // [설정 검증] 섹션 파싱
  const settingsMatch = rawBlock.match(/\[설정 검증\]([\s\S]*?)(?=\[|$)/);
  if (settingsMatch) {
    const section = settingsMatch[1];
    const worldCheck = section.match(/세계관 위반 요소 없음:\s*(.+)/)?.[1]?.trim();
    const villainCheck = section.match(/빌런 정체 노출 위험:\s*(.+)/)?.[1]?.trim();

    if (worldCheck || villainCheck) {
      result.settingsCheck = {
        worldConsistent: worldCheck?.includes('확인') || worldCheck?.includes('없') || true,
        villainSafe: villainCheck?.includes('없') || true,
      };
    }
  }

  console.log('[HiddenCoT] 로직 체크 파싱 완료:', {
    found: result.found,
    hasContinuity: !!result.continuityCheck,
    hasCharacter: !!result.characterCheck,
    hasPlot: !!result.plotCheck,
    hasSettings: !!result.settingsCheck,
  });

  return { cleanContent, logicCheck: result };
}

/**
 * 캐릭터 상태 변수 (JSON 추적용)
 */
export interface CharacterStatusTracker {
  characterId: string;
  characterName: string;
  lastUpdatedEpisode: number;
  status: {
    isAlive: boolean;
    location: string | null;
    emotionalState: string | null;
    injuries: string[];
    possessedItems: string[];
    relationships: Record<string, string>; // { characterName: relationshipType }
    skills: string[];
    secretsKnown: string[];
    customFields: Record<string, unknown>;
  };
  changelog: Array<{
    episodeNumber: number;
    field: string;
    oldValue: unknown;
    newValue: unknown;
    description: string;
  }>;
}

/**
 * 에피소드 분석 결과에서 캐릭터 상태 변화 추출
 *
 * @param logCompressionResult 로그 압축 결과 JSON
 * @param currentTrackers 현재 캐릭터 상태 추적기 배열
 * @param episodeNumber 현재 에피소드 번호
 * @returns 업데이트된 캐릭터 상태 추적기 배열
 */
export function updateCharacterStatusFromLog(
  logCompressionResult: {
    characterStates?: Record<string, { changes?: string[]; emotionalArc?: string }>;
    itemChanges?: { gained?: string[]; lost?: string[] };
  },
  currentTrackers: CharacterStatusTracker[],
  episodeNumber: number
): CharacterStatusTracker[] {
  const updatedTrackers = [...currentTrackers];

  if (logCompressionResult.characterStates) {
    for (const [charName, stateData] of Object.entries(logCompressionResult.characterStates)) {
      let tracker = updatedTrackers.find(t => t.characterName === charName);

      if (!tracker) {
        // 새 캐릭터 추적기 생성
        tracker = {
          characterId: `auto-${Date.now()}-${charName}`,
          characterName: charName,
          lastUpdatedEpisode: episodeNumber,
          status: {
            isAlive: true,
            location: null,
            emotionalState: null,
            injuries: [],
            possessedItems: [],
            relationships: {},
            skills: [],
            secretsKnown: [],
            customFields: {},
          },
          changelog: [],
        };
        updatedTrackers.push(tracker);
      }

      // 감정 상태 업데이트
      if (stateData.emotionalArc) {
        const oldValue = tracker.status.emotionalState;
        tracker.status.emotionalState = stateData.emotionalArc;
        tracker.changelog.push({
          episodeNumber,
          field: 'emotionalState',
          oldValue,
          newValue: stateData.emotionalArc,
          description: `감정 변화: ${stateData.emotionalArc}`,
        });
      }

      // 변화 목록 분석
      if (stateData.changes) {
        for (const change of stateData.changes) {
          // 부상 관련 변화 감지
          if (change.includes('부상') || change.includes('다침') || change.includes('상처')) {
            if (!tracker.status.injuries.includes(change)) {
              tracker.status.injuries.push(change);
              tracker.changelog.push({
                episodeNumber,
                field: 'injuries',
                oldValue: null,
                newValue: change,
                description: `부상 추가: ${change}`,
              });
            }
          }

          // 사망 감지
          if (change.includes('사망') || change.includes('죽음')) {
            tracker.status.isAlive = false;
            tracker.changelog.push({
              episodeNumber,
              field: 'isAlive',
              oldValue: true,
              newValue: false,
              description: '사망',
            });
          }

          // 위치 변화 감지
          if (change.includes('이동') || change.includes('도착') || change.includes('떠남')) {
            const oldLocation = tracker.status.location;
            tracker.status.location = change;
            tracker.changelog.push({
              episodeNumber,
              field: 'location',
              oldValue: oldLocation,
              newValue: change,
              description: `위치 변화: ${change}`,
            });
          }
        }
      }

      tracker.lastUpdatedEpisode = episodeNumber;
    }
  }

  // 아이템 변화 처리 (주인공에게 적용)
  if (logCompressionResult.itemChanges) {
    const protagonist = updatedTrackers.find(t =>
      t.characterName.includes('주인공') || updatedTrackers.indexOf(t) === 0
    );

    if (protagonist) {
      if (logCompressionResult.itemChanges.gained) {
        for (const item of logCompressionResult.itemChanges.gained) {
          if (!protagonist.status.possessedItems.includes(item)) {
            protagonist.status.possessedItems.push(item);
            protagonist.changelog.push({
              episodeNumber,
              field: 'possessedItems',
              oldValue: null,
              newValue: item,
              description: `아이템 획득: ${item}`,
            });
          }
        }
      }

      if (logCompressionResult.itemChanges.lost) {
        for (const item of logCompressionResult.itemChanges.lost) {
          const index = protagonist.status.possessedItems.indexOf(item);
          if (index > -1) {
            protagonist.status.possessedItems.splice(index, 1);
            protagonist.changelog.push({
              episodeNumber,
              field: 'possessedItems',
              oldValue: item,
              newValue: null,
              description: `아이템 상실: ${item}`,
            });
          }
        }
      }
    }
  }

  console.log('[CharacterStatusTracker] 상태 업데이트 완료:', {
    totalTrackers: updatedTrackers.length,
    episodeNumber,
  });

  return updatedTrackers;
}

/**
 * 캐릭터 상태 추적기를 프롬프트에 주입할 문자열로 변환
 */
export function serializeCharacterStatusForPrompt(trackers: CharacterStatusTracker[]): string {
  if (trackers.length === 0) return '';

  const sections: string[] = [];

  sections.push(`
╔═══════════════════════════════════════════════════════════════════════════════╗
║  📊 [자동 추적] 캐릭터 상태 변수 (Character Status Tracker)                    ║
╚═══════════════════════════════════════════════════════════════════════════════╝
`);

  for (const tracker of trackers) {
    let charSection = `
▶ ${tracker.characterName} (마지막 업데이트: ${tracker.lastUpdatedEpisode}화)
   - 생존: ${tracker.status.isAlive ? '생존' : '사망'}`;

    if (tracker.status.location) {
      charSection += `\n   - 위치: ${tracker.status.location}`;
    }
    if (tracker.status.emotionalState) {
      charSection += `\n   - 감정: ${tracker.status.emotionalState}`;
    }
    if (tracker.status.injuries.length > 0) {
      charSection += `\n   - 부상: ${tracker.status.injuries.join(', ')}`;
    }
    if (tracker.status.possessedItems.length > 0) {
      charSection += `\n   - 소지품: ${tracker.status.possessedItems.join(', ')}`;
    }

    // 최근 변화 로그 (최근 3개만)
    const recentChanges = tracker.changelog.slice(-3);
    if (recentChanges.length > 0) {
      charSection += `\n   - 최근 변화:`;
      for (const change of recentChanges) {
        charSection += `\n     • [${change.episodeNumber}화] ${change.description}`;
      }
    }

    sections.push(charSection);
  }

  return sections.join('\n');
}
